public void DoDuLieuVaoTable(){
        String CauLenhSQL = "Select * from LoaiKhachHang";
       ResultSet rs = ConnectSQL.ThucThiLenhReSultset(CauLenhSQL);
        
        DefaultTableModel tblModel = (DefaultTableModel)tblBangLHH.getModel();
//        xáo dữ liệu đang có trong bảng table 
        tblModel.setRowCount(0);
        Object obj[] = new Object[3];
        
        try {
            while(rs.next()){
                obj[0] = rs.getString("MaLoaiKhachHang");
                obj[1] = rs.getString("TenLoai");
                obj[2] = rs.getString("MoTa");
                tblModel.addRow(obj);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(new JFrame(), "Lỗi truy xuất dữ liệu");
        }
    }
