    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
      int viTriDuocChon = tblBangLHH.getSelectedRow();

        if(viTriDuocChon == -1){
            JOptionPane.showMessageDialog(new JFrame(), "Bạn chưa chọn dòng để sửa");
            return; //thoat khoi ham
        }  
        
           String tenLoai = txtTenLoai.getText();
      String mota= txtMoTa.getText();
        String maLKH = tblBangLHH.getValueAt(viTriDuocChon,0).toString();
        if(tenLoai.equals("") || mota.equals("")){
           JOptionPane.showMessageDialog(new JFrame(), "Chưa Nhập Đầy Đủ Thông Tin");
       }else{
        String CauLenhSua = "UPDATE [dbo].[LoaiKhachHang]  " +
"   SET [TenLoai] = N'"+tenLoai+"'  " +
"      ,[Mota] = N'"+mota+"'  " +
" WHERE MaLoaiKhachHang = " + maLKH;
          ConnectSQL.ThucThi3Lenh(CauLenhSua);
        DoDuLieuVaoTable();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

